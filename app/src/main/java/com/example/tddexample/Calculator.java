package com.example.tddexample;

public class Calculator {
    public Calculator() {
    }

    public int sum(int a, int b){
        return a+b;
    }

    public int sub(int a,int b){
        return a-b;
    }

    public int mul(int a,int b){
        return a*b;
    }

    public int div(int a,int b){
        return a/b;
    }
}
